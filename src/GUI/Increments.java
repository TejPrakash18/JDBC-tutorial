package GUI;

public class Increments {
    public static void main(String[] args) {
        int x =5;
        System.out.println(x++); //6
        System.out.println(++x); //7

        String str = "hello";
        String se = str.concat("World");
        System.out.println(str);
        System.out.println(se);
    }
}
